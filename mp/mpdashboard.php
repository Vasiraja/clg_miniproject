    <h3>Preparation Phase</h3>
<p>Students in this phase are equipped with soft and technical skills of fundamental importance expected of a graduate in engineering, technology, and management.</p><p>

<p><b>Students of I year</b> are trained in Fundamentals of Communicative English for a flair and command over the English language for a professional context. To achieve the same, two courses in English are offered in the I and II Semester. These courses ensure the effective use of the language in everyday professional tasks.</p><p>

<p><b>Students of II year</b> are sown with the seeds of superior capabilities that will speak of their personality. This is achieved through outbound trainings. The trainings prepare them to step off their comfort, rise up to the occasion, teach them to have multiple perspectives, the art of spontaneity, assertiveness, flexibility, creativity, and still carry the values that define the core of human existence, like compassion, empathy and gratitude. The beauty of this human engineering training is that they become the go-getters, come what may .</p><p>

<p><b>Students of III year,</b>  with the spadework in place, go for in-plant trainings in industries to get exposed to engineering and technology in action. They also, either in groups or individually do engineering and technology projects of a lighter quantum called minor projects. Most of these minor projects are extended researches of a previously done research by the senior students.</p><p>

<p><b>Students of IV year</b>  consider this year a momentous period of study with their involvement in industrial projects, software and hardware. They work in need based projects that are specified by various industries. They gain a substantial amount of engineering knowledge through this opportunity. They also intern with companies that are well versed.  KC Institute has nurtured association with companies and industries that opens an arena of choices to students for internships. Students who exhibit excellence during internships are recruited into the companies after successful completion of their degree. The exposure and hands-on experience gained at the work floors add value to the student's profile.</p>
